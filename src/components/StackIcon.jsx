const iconMap = {
  reactquery: { slug: 'reactquery', label: 'React Query', fallback: 'bi-database' },
  vitejs: { slug: 'vite', label: 'Vite' },
  bootstrap5: { slug: 'bootstrap', label: 'Bootstrap 5' },
  threejs: { slug: 'threedotjs', label: 'Three.js' },
  reactrouter: { slug: 'reactrouter', label: 'React Router' },
  react: { slug: 'react', label: 'React' },
  openai: { slug: 'openai', label: 'OpenAI' },
  netlify: { slug: 'netlify', label: 'Netlify' },
  git: { slug: 'git', label: 'Git' },
  arc: { slug: 'arc', label: 'Arc', fallback: 'bi-browser-chrome' },
}

function StackIcon({ name, className = '' }) {
  const [failed, setFailed] = useState(false)
  const key = (name || '').toLowerCase()
  const item = iconMap[key] || { slug: 'codeium', label: name || 'Unknown', fallback: 'bi-circle' }
  const src = `https://cdn.simpleicons.org/${item.slug}/A6ADBA`
  const useCustomOpenAI = key === 'openai'
  const useCustomNetlify = key === 'netlify'

  return (
    <div className={`stack-icon-root ${className}`.trim()}>
      {useCustomOpenAI ? (
        <CustomOpenAIIcon />
      ) : useCustomNetlify ? (
        <CustomNetlifyIcon />
      ) : !failed ? (
        <img
          src={src}
          alt={item.label}
          loading="lazy"
          width="30"
          height="30"
          onError={() => setFailed(true)}
        />
      ) : (
        <i className={`bi ${item.fallback}`} aria-hidden="true" />
      )}
      <span>{item.label}</span>
    </div>
  )
}

export default StackIcon
